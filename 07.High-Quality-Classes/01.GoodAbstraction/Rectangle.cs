namespace Abstraction
{
    using System;

    public class Rectangle : Figure
    {
        private double height;
        private double width;

        public Rectangle(double height, double width)
        {
            this.Height = height;
            this.Width = width;
        }

        public double Height
        {
            get
            {
                return this.height;
            }

            set
            {
                if (value <= MinRadiusHeightAndWidth)
                {
                    throw new ArgumentException("Height must be greater than zero to have valid rectangle!");
                }

                this.height = value;
            }
        }

        public double Width
        {
            get
            {
                return this.width;
            }

            set
            {
                if (value <= MinRadiusHeightAndWidth)
                {
                    throw new ArgumentException("Width must be greater than zero to have valid rectangle!");
                }

                this.width = value;
            }
        }

        public override double CalcPerimeter()
        {
            double perimeter = 2 * (this.Width + this.Height);
            return perimeter;
        }

        public override double CalcSurface()
        {
            double surface = this.Width * this.Height;
            return surface;
        }
    }
}